function pick(map: Record<string, Pair>, key: string, locale: Locale): string {
  if (locale === 'en' || key == null) return key;
  const hit = map[key];
  return hit ? hit[locale as 'de' | 'ja'] ?? key : key;
}
export const specLabel = (locale: Locale, key: string): string => pick(SPEC_LABELS, key, locale);
export const specVal = (locale: Locale, value: string): string => pick(SPEC_VALUES, value, locale);
export const tagText = (locale: Locale, tag: string): string => pick(TAGS, tag, locale);
